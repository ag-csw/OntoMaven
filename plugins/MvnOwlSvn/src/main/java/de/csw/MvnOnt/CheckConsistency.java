package de.csw.MvnOnt;

import java.net.MalformedURLException;
import java.net.URISyntaxException;
import java.net.URL;


import org.semanticweb.owlapi.apibinding.OWLManager;
import org.semanticweb.owlapi.model.IRI;
import org.semanticweb.owlapi.model.OWLOntology;
import org.semanticweb.owlapi.model.OWLOntologyCreationException;
import org.semanticweb.owlapi.model.OWLOntologyManager;
import org.semanticweb.owlapi.reasoner.ConsoleProgressMonitor;
import org.semanticweb.owlapi.reasoner.OWLReasoner;
import org.semanticweb.owlapi.reasoner.OWLReasonerConfiguration;
import org.semanticweb.owlapi.reasoner.OWLReasonerFactory;
import org.semanticweb.owlapi.reasoner.SimpleConfiguration;

import com.clarkparsia.pellet.owlapiv3.PelletReasonerFactory;




public class CheckConsistency {
	
	private OWLOntology ontology;
	private LoadOntology loader;
	private OWLOntologyManager manager;
	private URL uri ;
	private IRI ontologyIRI ;
	
	public boolean check(String owl_file){
		

		
		manager = OWLManager.createOWLOntologyManager();
		
		try {
			
			
			uri = new URL("file:///"+owl_file);
			ontologyIRI = IRI.create(uri);
			
			loader = new LoadOntology(manager,ontologyIRI);
			ontology = loader.loadOntology();
			
			
		} catch (MalformedURLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (URISyntaxException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (OWLOntologyCreationException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		
		
		OWLReasonerFactory reasonerFactory = new PelletReasonerFactory();
        ConsoleProgressMonitor progressMonitor = new ConsoleProgressMonitor();
	 	OWLReasonerConfiguration config = new SimpleConfiguration(progressMonitor);
        OWLReasoner reasoner = reasonerFactory.createReasoner(ontology, config);
         
		
		
	return reasoner.isConsistent();	
	
	}

}
